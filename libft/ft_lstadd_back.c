#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	last->next = new;
}
/** 
 Imagine [4] is the new node
 [1] -> [2] -> [3] -> NULL
 If list is empty, make new the first node
 Then, make last node next pointer to new node
**/
